<h3>Notes from Factors for Success with Microgreens -- Chris Thoreau</h3>
<h3>General Notes</h3>
	<div class="row">
		<ol class="col"><h4>Sanitation</h4>
			<li>Have a general sanitation plan.</li>
			<li>Two aspects of sanitation - human and plant pathogens</li>
			<li>Meticulous Handwashing</li>
			<li>Sanitize Everything</li>
			<li>Store things properly</li>
			<li>Document your sanitation</li>
			<li>Number one source of contamination for Microgreens is seed.</li>
			<li>Aquire seed specialized for sprouting and is pathogen tested.</li>
			<li>Controls -- temperature, humidity, lighting and airflow</li>
			<li>Storage -- must use within a week</li>
		</ol>
		<ol class="col"><h4>System for Everything</h4>
			<li>Develop an SOP -- written and video</li>
			<li>More efficient</li>
			<li>Makes Troubleshooting easier</li>
			<li>Easy to teach and monitor</li>
		</ol>
		<ol class="col"><h4>Crop Planning</h4>
			<li>Planning must be done weekly</li>
			<li>Check the model frequently</li>
			<li>Produce a little extra -- you can always give it away</li>
			<li>Record what you actually need</li>
		</ol>
		<ol calss="col"><h4>Get it Done</h4>
